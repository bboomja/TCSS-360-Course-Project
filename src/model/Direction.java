package src.model;

/**
 * Enum representing the directions for moving in the maze.
 * Each direction corresponds to a possible movement in the maze.
 * This enum is used to specify movement directions for players and game logic.
 * The directions are defined as follows:
 * - N: North or up
 * - S: South or Down
 * - E: East or Right
 * - W: West or left
 *
 * @author Hyun Jeon
 * @author Shuaib Ali
 * @author Yaxye Muxamed
 * @version Summer 2023
 */
public enum Direction {
    N, // North or Up
    S,// South or Down
    E,// East  // Right
    W  // West  // Left

}
